package money.Tracker.presentation.model;

public class DetailSchedule {
	private String category;
	private double detailBudget;

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public double getDetailBudget() {
		return detailBudget;
	}

	public void setDetailBudget(double detailBudget) {
		this.detailBudget = detailBudget;
	}
}
